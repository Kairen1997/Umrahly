# Function to seed itineraries for existing packages
seed_itineraries = fn ->
  # Get the first package to add sample itinerary
  packages = Packages.list_packages()

  if length(packages) > 0 do
    first_package = List.first(packages)

    case Packages.upsert_package_itineraries(first_package.id, sample_itinerary_data) do
      {:ok, itineraries} ->
        IO.puts("Successfully seeded #{length(itineraries)} itinerary days for package: #{first_package.name}")
        itineraries

      {:error, reason} ->
        IO.puts("Failed to seed itineraries: #{inspect(reason)}")
        []
    end
  else
    IO.puts("No packages found. Please create a package first.")
    []
  end
end

# Run the seeding
seed_itineraries.()
